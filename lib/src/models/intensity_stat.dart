class IntensityStat {
  final String from;
  final String to;

  final int? max;
  final int? average;
  final int? min;

  final String index;

  IntensityStat({
    required this.from,
    required this.to,
    required this.max,
    required this.average,
    required this.min,
    required this.index,
  });
}
